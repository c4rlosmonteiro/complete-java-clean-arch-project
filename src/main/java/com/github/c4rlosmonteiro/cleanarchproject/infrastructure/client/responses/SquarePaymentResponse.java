package com.github.c4rlosmonteiro.cleanarchproject.infrastructure.client.responses;

public final class SquarePaymentResponse {
    private String id;
    private String reference;
    private String status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getReference() {
        return reference;
    }

    public void setReference(String reference) {
        this.reference = reference;
    }
}

